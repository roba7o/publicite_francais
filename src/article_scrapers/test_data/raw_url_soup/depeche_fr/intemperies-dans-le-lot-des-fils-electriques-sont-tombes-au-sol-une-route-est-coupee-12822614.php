<p>Vers 19&nbsp;heures, &agrave; Frayssinet, des fils &eacute;lectriques &agrave; moyenne tension sont tomb&eacute;s sur la chauss&eacute;e. Pour &eacute;viter des accidents, la circulation sur la RD820, en direction de Cahors, a donc &eacute;t&eacute; coup&eacute;e dans les deux sens. Une d&eacute;viation est mise en place. Les pompiers du Lot &eacute;taient sur place pour s&eacute;curiser les lieux.</p><div id="ultimedia_wrapper" class="ultimedia_wrapper "></div>

<h2 class="txt-int">Le secteur de Gourdon impact&eacute;</h2>

<p>Cependant, vers 19h30, aucune coupure &eacute;lectrique n&rsquo;&eacute;tait signal&eacute;e.</p>

                
