<p>L&rsquo;&eacute;pisode orageux a commenc&eacute; quelques heures auparavant. Vers 17&nbsp;heures, le vent s&rsquo;est fortement lev&eacute; et le ciel grondait. De nombreuses chutes d&rsquo;arbre ont &eacute;t&eacute; signal&eacute;es, notamment sur le secteur de Gourdon, mobilisant la direction d&eacute;partementale des routes.</p>

